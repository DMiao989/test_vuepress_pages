## 引用、引用和术语定义
- <abbr> 对缩写进行标记，为浏览器、翻译系统以及搜索引擎提供有用的信息<p><abbr title="World Heath Organization">WHO<abbr>成立于1984年 </p>

- <acronym> 首字母缩写
- <address> 文章、文档的联系信息（作者、拥有者），斜体显示，浏览器在该元素前后添加换行；
- <bdo> 双流向覆盖，覆盖当前文本方向；<bdo dir="rtl"> This text will be written from right to left</bdo>
- <blockqute> 长的引用，浏览器会为其做缩进处理；
- <q> 短的引用，浏览器通常会为<q>元素包围引号；
- <cite> 引用、引证
